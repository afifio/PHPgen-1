Deutsche Version:
Vorraussetzungen
- php
- Webspace, in den mit PHP geschrieben werden kann.
- Einige einfache html Dateien ;)

Installation
- Entpacken Sie die Datei generator-*.tar in Ihren öffentlichen Ordner "/var/www/" zum Beispiel.
- Benennen Sie den entpackten Ordner "generator" nach Ihren Belangen um in z.B. <br />webserver, server, web oder was immer Sie brauchen oder für am passendsten halten.
- Machen Sie den Ordner "pages" darin beschreibbar von dem Benutzer Ihres <br />Webservers (oft www-data).
- Konfigurieren Sie den Generator durch editieren der Datei config.php.
- Kopieren Sie ihre einfachen HTML Dateien in den Pages Ordner.
- Überprüfen Sie ihre Installation durch aufrufen Ihres Ordners in Ihrem Browser, 
  und wenn die Kofiguration funktioniert hat -> Fertig!

English Version:
requirements
- php
- webspace where the webserver can write into the file system
- some simple html files ;)

installation
- uncompress generator-*.tar in your html public directory "/var/www/" for example. 
- rename the uncompressed directory "generator" to your favorite output directory 
  eg. webserver, server, web whatever you want.
- make that directory "pages" writeable for the webservers user (often www-data).
- configure Generator by editing the config.php file.
- copy your simple HTML-Files into the pages directory 
- check your installation by browsing your chossen directoy by your webbrowser
  and if your config works -> READY!

