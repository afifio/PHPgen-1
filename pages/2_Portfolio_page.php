    <h1>Generator-FAQ</h1>
  </header>

  <p><b>Was ist der Generator?</b> - Generator ist ein Webseiten-Verwaltungs-System fü Ihren Webspace. Es erstellt, verwaltet und editiert Dateien und gibt diese als PHP Webseiten aus. </p>
  <p><b>Was ist das Hauptziel dieses Projektes?</b> - Eine einfache möglichkeit einfache Dateien als Basis für www-Webseiten zu verwenden.</p>
  <p><b>Welche Lizenz wird beim Generator verwendet?</b> - Generator ist publiziert unter den Bedingungen der GNU General Public Licence (GPL).</p>
  <p><b>Kann ich Generator unterstützen?</b> - Ja du kannst!
    <ul>
      <li>Schreibe ein wenig Code in PHP. (Multilanguage Support, erstelle AJAX Features, etc...)</li>
      <li>Erstelle ein neues Theme mit CSS.</li>
      <li>Spende für das Projekt.</li>
    </ul>

  <p><b>Ich habe Probleme, bekomme ich irgendwo Hilfe?</b> - Ja, schreibe eine englische oder deutsche Email an die Hauptentwickler <a href="mailto:vaddi@exigem.de">vaddi@exigem.de</a> oder erstelle ein Ticket in unserem <a href="http://support.exigem.com/">Supportsystem</a>.</p>


