  <h1>Webseiten Generator als Basis</h1>
  <a name="001"></a>
</header>

<p>Wir haben uns für die Nutzung des <a href="http://www.exigem.com/lab/gen/">Webseiten Generators</a> als Basis CMS entschieden. So haben wir hier auch die Möglichkeit die Entwicklung aktiv zu beschleunigen. Wovon wiederum die Anwender profitieren werden. Zudem arbeitet das System ohne Datenbank und ist gerade für einfache kleine Webauftritte ideal geeignet.</p>

