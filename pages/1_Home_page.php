<header>
  <time datetime="2011-11-12" pubdate>12.November 2011</time>
  <h1>Wer sollte den Generator verwenden? </h1>
  <a name="002"></a>
</header>

<p>Wir haben einige neue Funktionen hier vorerst zum testen eingebaut. Die Skripte und die Funktionsaufrufe wurden weiter angepasst und laufen nun stabil. Hier eine kleine Liste um selbst den Überblick nicht zu verlieren:</p>

<ul>
  <li><span style="color:#0b0;">Intro beim ersten Seitenaufruf</span></li>
  <li><span style="color:#0b0;">Kontaktformular mit Benutzerfreundlicher Fehlerhandhabung und schicker HTML-Mail</span></li>
  <li>News Seite mit RSS/Atom Feed Einbindung/Ausgabe</li>
  <li>Uploadfunktion für Dateien</li>
  <li><span style="color:#0b0;">Validator HTML & CSS im Footer anzeigen (valides HTML5/CSS3)</span></li>
  <li><span style="color:#0b0;">Benutzerfreundliche Backupfunktion</span></li>
  <li>Plugin Konfiguration und Verwaltung</li>
  <li>Theme Konfiguration und Verwaltung</li>
  <li><span style="color:#0b0;">Kundendatei, sollte Editierbar in eine netten Liste angezeigt werden</span></li>
  <li>Benutzerverwaltung mit Registrierenfunktion</li>
  <li>Passwort vergessen wäre eine nette Sache</li>
  <li>Menüführung sollte auch Unterseiten verwenden</li>
    <li><span style="color:#0b0;">Syntax Highlighting im Backend dank <a href="http://codemirror.net/">CodeMirror</a></span></li>

</ul>
