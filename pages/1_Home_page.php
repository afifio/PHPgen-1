  <h1>Warum verwendet ihr den Generator?</h1>
  <a name="003"></a>
</header>

<p>Wir waren wieder fleißig und haben unter anderem ein Intro, einen Validator (Html & CSS) im Footerbereich und ein Benutzerfreundliches Kontaktformular entworfen. Alle diese Techniken sind via javascript eingebunden und so gestaltet, das man die Seite jedoch auch ohne aktiviertes Javascript uneingeschränkt verwenden kann.</p>

